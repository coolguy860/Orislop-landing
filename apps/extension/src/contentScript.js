  function extractCandidate(element) {
    const link = findBestVideoLink(element);
    const title = findTitle(element, link);
    const channelName = findChannelName(element);
    const durationSeconds = findDurationSeconds(element);
    const visibleText = normalizeWhitespace(element.textContent || "");
    const aiDisclosureText = findVisibleAiDisclosureText(element);
    const parsed = parseYouTubeUrl(link || window.location.href);

    return {
      videoId: parsed.videoId,
      url: parsed.normalizedUrl || link || window.location.href,
      title,
      channelName,
      durationSeconds,
      visibleText: normalizeWhitespace([visibleText, aiDisclosureText].filter(Boolean).join(" ")),
      aiDisclosureText
    };
  }

  function findVisibleAiDisclosureText(element) {
    const candidates = [];
    const localText = normalizeWhitespace(element.textContent || "");
    if (hasPlatformAiDisclosure(localText)) {
      candidates.push(localText);
    }

    const disclosureSelectors = [
      "yt-formatted-string",
      "ytd-badge-supported-renderer",
      "ytd-metadata-row-renderer",
      "ytd-video-attribute-view-model",
      "tp-yt-paper-tooltip",
      "[aria-label]",
      "[title]"
    ];
    for (const node of element.querySelectorAll(disclosureSelectors.join(","))) {
      if (!(node instanceof HTMLElement) || !isProbablyVisible(node)) {
        continue;
      }
      const text = normalizeWhitespace([
        node.textContent || "",
        node.getAttribute("aria-label") || "",
        node.getAttribute("title") || ""
      ].join(" "));
      if (hasPlatformAiDisclosure(text)) {
        candidates.push(text);
      }
    }

    if (isCurrentVideoCandidate(element, { videoId: parseYouTubeUrl(window.location.href).videoId })) {
      const pageText = getCurrentPageAiDisclosureText();
      if (hasPlatformAiDisclosure(pageText)) {
        candidates.push(pageText);
      }
    }

    return candidates.length > 0 ? candidates[0].slice(0, 1000) : "";
  }

  function getCurrentPageAiDisclosureText() {
    const now = Date.now();
    if (pageAiDisclosureCache.href === window.location.href && now - pageAiDisclosureCache.at < 1200) {
      return pageAiDisclosureCache.text;
    }

    const selectors = [
      "ytd-watch-metadata",
      "ytd-metadata-row-container-renderer",
      "ytd-video-attribute-view-model",
      "ytd-badge-supported-renderer",
      "ytd-engagement-panel-section-list-renderer",
      "ytd-shorts [aria-label]",
      "ytd-shorts yt-formatted-string",
      "ytm-watch",
      "ytm-badge"
    ];
    const text = normalizeWhitespace(Array.from(document.querySelectorAll(selectors.join(",")))
      .slice(0, 80)
      .map((node) => node.textContent || node.getAttribute?.("aria-label") || "")
      .join(" "));
    pageAiDisclosureCache = {
      href: window.location.href,
      at: now,
      text
    };
    return text;
  }

  function findBestVideoLink(element) {
    const anchors = Array.from(element.querySelectorAll("a[href]"));
    const link = anchors.find((anchor) => {
      const href = anchor.getAttribute("href") || "";
      return href.includes("/watch?v=") || href.includes("/shorts/");
    });
    if (!link) {
      return null;
    }

    const href = link.getAttribute("href") || "";
    try {
      return new URL(href, window.location.origin).href;
    } catch {
      return null;
    }
  }

  function findTitle(element, link) {
    const titleElement = element.querySelector([
      "#video-title",
      "yt-formatted-string#video-title",
      "h3",
      "a[title]",
      "[aria-label][role='link']",
      "yt-lockup-metadata-view-model a"
    ].join(","));
    const title = titleElement?.getAttribute("title") || titleElement?.textContent || "";
    if (title.trim()) {
      return normalizeWhitespace(title);
    }

    const linkedAnchor = link
      ? Array.from(element.querySelectorAll("a[href]")).find((anchor) => {
        try {
          return new URL(anchor.getAttribute("href") || "", window.location.origin).href === link;
        } catch {
          return false;
        }
      })
      : null;
    return normalizeWhitespace(linkedAnchor?.textContent || element.getAttribute("aria-label") || "");
  }

