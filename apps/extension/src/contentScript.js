  function setupCandidateObserver() {
    if (!("IntersectionObserver" in window)) {
      return;
    }

    candidateObserver = new IntersectionObserver((entries) => {
      for (const entry of entries) {
        if (!(entry.target instanceof HTMLElement)) {
          continue;
        }
        if (entry.isIntersecting) {
          visibleCandidateElements.add(entry.target);
        } else {
          visibleCandidateElements.delete(entry.target);
        }
      }
      if (entries.some((entry) => entry.isIntersecting)) {
        scheduleScan();
      }
    }, {
      root: null,
      rootMargin: "900px 0px 1800px 0px",
      threshold: 0.01
    });
  }

  function scheduleCandidateObservation() {
    if (!candidateObserver) {
      return;
    }
    window.clearTimeout(observeTimer);
    observeTimer = window.setTimeout(observeCandidateElements, Math.max(SCAN_DEBOUNCE_MS, 500));
  }

  function observeCandidateElements() {
    if (!candidateObserver) {
      return;
    }

    for (const element of findCandidateElements().slice(0, MAX_SCAN_PER_PASS * 2)) {
      if (observedCandidateElements.has(element)) {
        continue;
      }
      observedCandidateElements.add(element);
      candidateObserver.observe(element);
    }
  }

