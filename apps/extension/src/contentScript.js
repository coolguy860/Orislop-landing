  function findDurationSeconds(element) {
    const durationElement = element.querySelector([
      "ytd-thumbnail-overlay-time-status-renderer",
      "span.ytd-thumbnail-overlay-time-status-renderer",
      ".ytd-thumbnail-overlay-time-status-renderer",
      ".badge-shape-wiz__text",
      "[aria-label*='minute']",
      "[aria-label*='second']"
    ].join(","));
    const raw = normalizeWhitespace(durationElement?.textContent || durationElement?.getAttribute?.("aria-label") || "");
    return parseDurationSeconds(raw);
  }

  function parseDurationSeconds(value) {
    const match = String(value || "").match(/\b(?:(\d{1,2}):)?(\d{1,2}):(\d{2})\b/);
    if (match) {
      const hours = Number(match[1] || 0);
      const minutes = Number(match[2] || 0);
      const seconds = Number(match[3] || 0);
      return hours * 3600 + minutes * 60 + seconds;
    }
    const minuteMatch = String(value || "").match(/\b(\d+)\s*minute/i);
    const secondMatch = String(value || "").match(/\b(\d+)\s*second/i);
    if (minuteMatch || secondMatch) {
      return Number(minuteMatch?.[1] || 0) * 60 + Number(secondMatch?.[1] || 0);
    }
    return null;
  }

