    const existing = element.querySelector(":scope > .orislop-hidden-comment");
    if (existing) {
      existing.style.display = "";
      return;
    }

    Array.from(element.children).forEach((child) => {
      if (child instanceof HTMLElement) {
        rememberChildDisplay(child);
        child.style.display = "none";
      }
    });

    const shield = document.createElement("section");
    shield.className = "orislop-hidden-comment";
    shield.setAttribute("role", "region");
    shield.setAttribute("aria-label", "Likely bot comment hidden by Orislop");

