  async function attemptAutoSkip(extracted, score) {
    if (!skipController) {
      return;
    }

    const decision = skipController.start({
      recommendation: score.recommendation,
      autoSkip: settingsCache.autoSkip,
      videoId: extracted.videoId,
      attempt: ({ attemptIndex }) => attemptNavigationStep(attemptIndex)
    });
    if (!decision.allowed) {
      showAutoSkipGuard(extracted, score, decision.reason);
      return;
    }

    skippedThisSession += 1;
    showCurrentSkipShield(extracted, score, {
      statusMessage: `Moving to the next video. Safety stop: ${skippedThisSession}/${MAX_CONSECUTIVE_AUTOSKIPS} consecutive skips.`
    });
    showAutoSkipToast(extracted, score, `Auto-skip started for a Skip-rated video: ${extracted.title || "YouTube video"}.`);
    await saveSkippedRecord(extracted, score, currentVideoKind() === "short" ? "auto_skipped_short" : "auto_skipped_watch");
  }

  function attemptNavigationStep(attemptIndex) {
    return currentVideoKind() === "short"
      ? tryNextShortNavigation(attemptIndex)
      : tryNextWatchNavigation(attemptIndex);
  }

  function tryNextShortNavigation(attemptIndex) {
    if (attemptIndex === 0) {
      return clickFirst([
        "#navigation-button-down button",
        "button[aria-label='Next video']",
        "button[aria-label='Next Short']",
        "button[aria-label='Next']",
        "ytd-button-renderer[aria-label='Next video'] button"
      ]);
    }
    if (attemptIndex === 1) {
      dispatchNavigationKey("ArrowDown", 40);
      return false;
    }
    if (attemptIndex === 2) {
      dispatchNavigationKey("PageDown", 34);
      return false;
    }
    dispatchWheelNavigation();
    return false;
  }

  function tryNextWatchNavigation(attemptIndex) {
    if (attemptIndex === 0) {
      return clickFirst([
        ".ytp-next-button",
        "a.ytp-next-button",
        "button.ytp-next-button",
        "a[aria-label='Next']",
        "button[aria-label='Next']",
        "a[href*='watch?v='][aria-label*='Next']"
      ]);
    }
    if (attemptIndex === 1) {
      dispatchNavigationKey("N", 78, true);
    }
    return false;
  }

  function dispatchNavigationKey(key, keyCode, shiftKey = false) {
    const eventOptions = {
      key,
      code: key === "ArrowDown" ? "ArrowDown" : key === "PageDown" ? "PageDown" : "KeyN",
      keyCode,
      which: keyCode,
      cancelable: true,
      bubbles: true,
      shiftKey
    };
    const active = document.activeElement;
    const isEditing = active instanceof HTMLInputElement
      || active instanceof HTMLTextAreaElement
      || active?.isContentEditable;
    const target = isEditing ? document.body : active || document.body;
    target?.dispatchEvent(new KeyboardEvent("keydown", eventOptions));
    target?.dispatchEvent(new KeyboardEvent("keyup", eventOptions));
  }

  function dispatchWheelNavigation() {
    const event = new WheelEvent("wheel", {
      bubbles: true,
      cancelable: true,
      deltaY: Math.max(window.innerHeight, 900),
      deltaMode: 0
    });
    (document.activeElement || document.documentElement).dispatchEvent(event);
  }

  function clickFirst(selectors) {
    for (const selector of selectors) {
      const target = document.querySelector(selector);
      if (target instanceof HTMLElement && !target.hasAttribute("disabled") && isProbablyVisible(target)) {
        target.click();
        return true;
      }
    }
    return false;
  }

